<?php


namespace PatchManager\Exception;


use PatchManager\OperationData;
use Exception;

class MissingOperationNameRequest extends \Exception
{
    public function __construct(OperationData $operationData, $message = "", $code = 0, Exception $previous = null)
    {
        $message = "You passed an operation without a name to the PatchManager. The json should contains
            an 'op' key. Here is the patch operation that failed: ".json_encode($operationData);
        parent::__construct($message, $code, $previous); // TODO: Change the autogenerated stub
    }
} 